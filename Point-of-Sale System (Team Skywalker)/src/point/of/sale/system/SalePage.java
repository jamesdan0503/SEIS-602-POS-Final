    private void jb2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jb2ActionPerformed
        int appleNum = 0, bananaNum = 0, cherryNum = 0;
        long appleNum_inv = 0, bananaNum_inv = 0, cherryNum_inv = 0;

        if (appleCheck.isSelected()) {
            if ((appleText.getText().matches("\\D+")) || appleText.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "input invalid", "WARNING_MESSAGE", JOptionPane.WARNING_MESSAGE);
            }
            appleNum = Integer.parseInt(appleText.getText());
        }
        if (bananaCheck.isSelected()) {
            if ((bananaText.getText().matches("\\D+")) || bananaText.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "input invalid", "WARNING_MESSAGE", JOptionPane.WARNING_MESSAGE);
            }
            bananaNum = Integer.parseInt(bananaText.getText());
        }
        if (cherryCheck.isSelected()) {
            if ((cherryText.getText().matches("\\D+")) || cherryText.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "input invalid", "WARNING_MESSAGE", JOptionPane.WARNING_MESSAGE);
            }
            cherryNum = Integer.parseInt(cherryText.getText());
        }

        this.writeTransaction(appleNum, bananaNum, cherryNum);
        JSONParser parser = new JSONParser();
        try {

            Object obj = parser.parse(new FileReader("C:\\SEIS602 Java\\inventory\\info.json"));
            JSONObject jobj = (JSONObject) obj;
            appleNum_inv = (long) jobj.get("apple");
            bananaNum_inv = (long) jobj.get("banana");
            cherryNum_inv = (long) jobj.get("cherry");
        } catch (Exception e) {
            e.printStackTrace();
        }

        appleNum_inv = appleNum_inv - appleNum;
        if (appleNum_inv <= 20) {
            appleNum_inv += 50;
            JOptionPane.showMessageDialog(null, "Apple Low Inventory" + "\nAuto Update to  " + appleNum_inv, "WARNING_MESSAGE", JOptionPane.WARNING_MESSAGE);
        }
        bananaNum_inv = bananaNum_inv - bananaNum;
        if (bananaNum_inv <= 20) {
            bananaNum_inv += 50;
            JOptionPane.showMessageDialog(null, "Banana Low Inventory" + "\nAuto Update to  " + bananaNum_inv, "WARNING_MESSAGE", JOptionPane.WARNING_MESSAGE);
        }
        cherryNum_inv = cherryNum_inv - cherryNum;
        if (cherryNum_inv <= 20) {
            cherryNum_inv += 50;
            JOptionPane.showMessageDialog(null, "Cherry Low Inventory" + "\nAuto Update to  " + cherryNum_inv, "WARNING_MESSAGE", JOptionPane.WARNING_MESSAGE);
        }

        JsonWR inventoryChange = new JsonWR();
        inventoryChange.inventoryWrite(inventoryChange.json_obj(appleNum_inv, bananaNum_inv, cherryNum_inv));
        JsonWR historyChange = new JsonWR();
        historyChange.historyWrite(historyChange.json_obj_history(cashierName, appleNum, bananaNum, cherryNum, this.totalCalc()));
    }//GEN-LAST:event_jb2ActionPerformed

