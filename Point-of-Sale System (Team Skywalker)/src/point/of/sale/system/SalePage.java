    public void writeTransaction(int appleNum, int bananaNum, int cherryNum) {
        File file = new File("C:\\SEIS602 Java\\transaction");
        File[] files = file.listFiles();
        int num = 100;
        for (File f : files) {
            if (Integer.parseInt(f.getName().substring(0, 3)) > num) {
                num = Integer.parseInt(f.getName().substring(0, 3));
            }
        }

        JsonWR jsonwriter = new JsonWR();
        jsonwriter.transactionWrite(jsonwriter.json_obj(appleNum, bananaNum, cherryNum),
                "C:\\SEIS602 Java\\transaction\\", num + 1);

        int transN = num + 1;
        jLabel3.setText("Transaction No. : " + transN);
    }

